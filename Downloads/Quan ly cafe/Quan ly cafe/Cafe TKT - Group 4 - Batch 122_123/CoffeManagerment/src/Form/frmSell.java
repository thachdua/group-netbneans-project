    private void btnSearch1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearch1ActionPerformed
        tableSell.setEnabled(true);
        String fullName = txtfullname.getText();
        boolean check = true;
        if (fullName.equals("")) {
            check = false;
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập họ tên !");
        }
        if (check) {
            listbyFullname = order.showbyFullname(fullName);

            Vector data = new Vector();
            for (Order sr : listbyFullname) {
                Vector row = new Vector();
                row.add(sr.getOrderId());
                row.add(sr.getSeller());
                row.add(sr.getSellDate());
                row.add(sr.getTotal());

                data.add(row);
            }
            tableSell.setModel(new DefaultTableModel(data, head));
        }
    }//GEN-LAST:event_btnSearch1ActionPerformed

