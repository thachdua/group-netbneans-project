    public void showSell() {
        listOrder = order.showSell();

        Vector data = new Vector();
        for (Order sr : listOrder) {
            Vector row = new Vector();
            row.add(sr.getOrderId());
            row.add(sr.getSeller());
            row.add(sr.getSellDate());
            row.add(sr.getTotal());

            data.add(row);
        }
        tableSell.setModel(new DefaultTableModel(data, head));
    }

