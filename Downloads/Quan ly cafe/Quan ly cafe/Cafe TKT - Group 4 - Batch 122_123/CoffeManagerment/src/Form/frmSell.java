    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        tableSell.setEnabled(true);
        Date date = txtDate.getDate();
        SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
        String dateString = formatter.format(date);
        boolean check = true;
        if (date.equals("")) {
            check = false;
            JOptionPane.showMessageDialog(this, "Chưa có dữ liệu !");

        }
        if (check) {
            listbydate = order.showBydate(dateString);

            Vector data = new Vector();
            for (Order sr : listbydate) {
                Vector row = new Vector();
                row.add(sr.getOrderId());
                row.add(sr.getSeller());
                row.add(sr.getSellDate());
                row.add(sr.getTotal());

                data.add(row);
            }
            tableSell.setModel(new DefaultTableModel(data, head));
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        tableSell.setEnabled(true);
        int giatien = 0;
        for (int i = 0; i < tableSell.getRowCount(); i++) {

            int total = (Integer) tableSell.getValueAt(i, 3);
            giatien = giatien + total;

        }
        if (giatien != 0) {
            Total totals = new Total();
            totals.setTotal(giatien);
            frmTotal vippay = new frmTotal(this, totals);
            vippay.setVisible(true);
        }


    }//GEN-LAST:event_jButton2ActionPerformed


