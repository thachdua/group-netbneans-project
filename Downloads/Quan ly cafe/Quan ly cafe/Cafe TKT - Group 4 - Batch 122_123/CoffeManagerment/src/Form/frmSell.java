    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        data1.removeAll(data1);
        if (orderid.equals("")) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy dữ liệu ?");
        } else {
            listbyOrderId = order.showbyOrderId(orderid);

            Vector data = new Vector();
            for (OrderDetail sr : listbyOrderId) {

                Vector row = new Vector();
                row.add(sr.getOrderId());
                row.add(sr.getSerName());
                row.add(sr.getQuantity());
                row.add(sr.getPrice());
                row.add(sr.getNote());
                row.add(sr.getSercode());

                data1.add(row);

                tableSell.setModel(new DefaultTableModel(data1, head1));
                tableSell.setEnabled(false);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

